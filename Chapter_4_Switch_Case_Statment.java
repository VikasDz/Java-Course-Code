package com.CodeWithMe;

public class Chapter_4_Switch_Case_Statment {
    public static void main(String[] args) {
        int a = 45;
        switch (a){
            case 40: {
                System.out.println("This is 40");
                break;
            }
            case 45:{
                System.out.println("This is 45");
                break;
            }
            case 5:{
                System.out.println("This is 5");
                break;
            }
        }
    }
}
